package bitcom.sicapil.data;

public class DataJenisPengurusan {

    public String id_jenis_pengurusan;
    public String jenis_pengurusan;
    public String status_aktif;
    public String url_icon;
    public String label;

    public String get_id_jenis_pengurusan() { return id_jenis_pengurusan; }
    public String get_jenis_pengurusan() { return jenis_pengurusan; }
    public String get_status_aktif() { return status_aktif; }
    public String get_url_icon() { return url_icon; }
    public String get_label() { return label; }

    public void set_id_jenis_pengurusan(String id_jenis_pengurusan) { this.id_jenis_pengurusan = id_jenis_pengurusan; }
    public void set_jenis_pengurusan(String jenis_pengurusan) { this.jenis_pengurusan = jenis_pengurusan; }
    public void set_status_aktif(String status_aktif) { this.status_aktif = status_aktif; }
    public void set_url_icon(String url_icon) { this.url_icon = url_icon; }
    public void set_label(String label) { this.label = label; }

}
